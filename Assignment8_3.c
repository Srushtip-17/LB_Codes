#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name : KMToMeter
// Input :         int
// Output :        int
// Description :   Write a program which accepts distance in km and converts it into meter. (1km = 1000 Meter)
// language :      c
// Date :          28/05/2026
// Author :        Srushti Nitin Pagar
//
//////////////////////////////////////////////////////////////////////////////////////////////

int KMToMeter(int iNo)                                      
{
    int iMeter = 0;

    iMeter = iNo * 1000;
    
    return iMeter;
}

//////////////////////////////////////////////////////////////////////////////////////////
// 
// Write a program which accepts distance in km and converts it into meter.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter distance in km : ");
    scanf("%d", &iValue);
    
    iRet = KMToMeter(iValue);
    
    printf("Distance in meter is : %d\n", iRet);

    return 0;
}

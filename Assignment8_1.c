#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name : CircleArea
// Input :         float
// Output :        double
// Description :   Write a program which accepts radius of circle from user and calculates its area. Consider value of PI as 3.14
// language :      c
// Date :          28/05/2026

class NumberX
{
    void FactRev(int iNo)
    {
        int iCnt = 0;

        for(iCnt = iNo - 1 ; iCnt > 0 ; iCnt--)
        {
            if(iNo % iCnt == 0)
            {
                System.out.println(iCnt);
            }
        }
    }
} 


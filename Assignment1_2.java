public class Assignment1_2 
{
    public static void main(String[] args) 
    {
        Display();
    }

    public static void Display()            //if we declare this function as static then we can call it directly without creating object of class
    {
        for (int i = 1; i <= 5; i++) 
        {
            System.out.println("Marvellous");
        }
    }
}

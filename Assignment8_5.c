#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name : SquareMeter
// Input :         int
// Output :        double
// Description :   Write a program which accepts area in square feet and converts it into square meter. (1 square feet = 0.0929 Square Meter)
// language :      c
// Date :          28/05/2026
// Author :        Srushti Nitin Pagar
//
//////////////////////////////////////////////////////////////////////////////////////////////

double SquareMeter(int iNo)                                      
{
    double dArea;

    dArea = iNo * 0.0929;
    
    return dArea;
}

//////////////////////////////////////////////////////////////////////////////////////////
// 
// Write a program which accepts area in square feet and converts it into square meter. (1 square feet = 0.0929 Square Meter)
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet : ");
    scanf("%d", &iValue);

    dRet = SquareMeter(iValue);

    printf("Area in square meters is : %.4lf\n", dRet);

    return 0;
}

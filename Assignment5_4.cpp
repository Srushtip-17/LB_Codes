#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////////////////
//  
//  Function name : Display
//  Input :         int
//  Output :        void
//  Description :   write a program which accept number from user and print all the odd number till that number on screen
//  language :      C++
//  Date :          27/05/2026

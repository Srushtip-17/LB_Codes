////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;
    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if(iCnt % 2 != 0)
        {
            cout<<iCnt<<" ";
        }
    }
}

int main()
{
    int iValue = 0;

    cout<<"Enter Number to print its number line : ";
    cin>>iValue;

    Display(iValue);

    return 0;
}

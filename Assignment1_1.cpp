int Divide(int iNo1 , int iNo2)
{
    int iAns = 0;

    if(iNo2 == 0)
    {
        return -1;
    }
    iAns = iNo1 / iNo2 ;

    return iAns;
}

////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application to Perform Division of 2 integers
//
////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0;
    int iValue2 = 0;
    int iRet = 0;

    cout<<"Enter First Number for Division : ";
    cin>>iValue1;

    cout<<"Enter Second Number for Division : ";
    cin>>iValue2;

    iRet = Divide(iValue1 , iValue2);

    cout<<"Division is : "<<iRet;

    return 0;
}

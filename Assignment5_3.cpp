void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt = -iNo; iCnt <= iNo; iCnt++)
    {
        cout<<iCnt<<" ";
    }
}

int main()
{
    int iValue = 0;

    cout<<"Enter Number to print its number line : ";
    cin>>iValue;

    Display(iValue);

    return 0;
}
